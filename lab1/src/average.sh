#!/bin/bash

# Проверяем наличие аргументов
if [ $# -eq 0 ]; then
    echo "Ошибка: Не предоставлено ни одного аргумента."
    echo "Использование: $0 число1 число2 ... числоN"
    exit 1
fi

# Переменные
count=$# # Количество аргументов
sum=0 # Среднее арифметическое входных аргументов

# Считаем сумму всех аргументов
for num in "$@"; do
    # Используем калькулятор bc для работы с дробными числами
    sum=$(echo "$sum + $num" | bc -l)
done

# Вычисляем среднее арифметическое
average=$(echo "$sum / $count" | bc -l)

# Выводим результаты
echo "Количество чисел: $count"
echo "Среднее арифметическое: $average"
